x = 12
y = 5

#soma 2 + 3 = 5
print(f"Soma:  {x + y}")

#subtração
print(f"Subtração:  {x - y}")

# multiplicação
print(f"Multiplicação:  {x * y}")

#divisão
print(f"Divisão:  {x / y}")

#divisão parte inteira
print(f"Divisão parte inteira:  {x // y}")

# resto da divisão
print(f"Resto da divisão:  {x % y}")

# exponenciação
print(f"Exponenciação:  {x ** y}")


# manutenção de sinal
print(f"Manutenção de sinal:  {+(x - 22)}")

# inversão de sinal
print(f"Inversão de sinal:  {-(x - 22)}")

