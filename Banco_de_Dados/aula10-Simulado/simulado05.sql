 -- Selecionar os dados dos colaboradores ordenando pelo nome em ordem crescente, e ordenando pelo sobrenome em ordem decrescente. 
SELECT * FROM colaborador 
ORDER BY nome , sobrenome DESC;

SELECT * FROM colaborador 
ORDER BY nome DESC, sobrenome DESC;

SELECT * FROM colaborador 
ORDER BY nome DESC, sobrenome ASC;

SELECT * FROM colaborador 
ORDER BY nome ASC, sobrenome ASC;

SELECT * FROM colaborador 
ORDER BY nome , sobrenome;

SELECT * FROM colaborador 
ORDER BY nome DESC, sobrenome;

SELECT * FROM colaborador 
ORDER BY nome ASC, sobrenome;

SELECT * FROM colaborador 
ORDER BY nome , sobrenome ASC;